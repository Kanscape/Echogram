import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const appRoot = path.resolve(fileURLToPath(new URL('..', import.meta.url)));
const libRoot = path.join(appRoot, 'lib');
const iconsRoot = path.join(appRoot, 'node_modules', 'lucide-static', 'icons');
const outputFile = path.join(libRoot, 'components', 'lucide_icon.g.dart');

const supportedTags = new Set(['path', 'circle', 'rect', 'line', 'polyline', 'polygon', 'ellipse']);
const positionalLast = new Set(['path', 'circle', 'rect', 'line', 'polyline', 'polygon', 'ellipse']);
const directAttrs = new Set([
  'd',
  'points',
  'x',
  'y',
  'x1',
  'y1',
  'x2',
  'y2',
  'cx',
  'cy',
  'r',
  'rx',
  'ry',
  'width',
  'height',
]);

async function main() {
  const dartFiles = await collectDartFiles(libRoot);
  const iconNames = new Set();

  for (const file of dartFiles) {
    const source = await fs.readFile(file, 'utf8');
    const matches = source.matchAll(/LucideIcon\(\s*'([a-z0-9-]+)'/g);
    for (const match of matches) {
      iconNames.add(match[1]);
    }
  }

  const sortedIcons = [...iconNames].sort();
  const cases = [];

  for (const iconName of sortedIcons) {
    const svgPath = path.join(iconsRoot, `${iconName}.svg`);
    let svgSource;
    try {
      svgSource = await fs.readFile(svgPath, 'utf8');
    } catch {
      throw new Error(`Lucide icon "${iconName}" was referenced in source but not found at ${svgPath}`);
    }

    const body = extractSvgBody(svgSource);
    const nodes = parseSvgNodes(body);
    cases.push(renderCase(iconName, nodes));
  }

  const output = `// GENERATED FILE - DO NOT EDIT.\n` +
      `// Generated by tool/generate-lucide-icons.mjs from lucide-static.\n\n` +
      `part of 'lucide_icon.dart';\n\n` +
      `List<Component>? _buildLucideNodes(String name) {\n` +
      `  switch (name) {\n` +
      (cases.length > 0 ? `${cases.join('\n')}\n` : '') +
      `    default:\n` +
      `      return null;\n` +
      `  }\n` +
      `}\n`;

  await fs.mkdir(path.dirname(outputFile), { recursive: true });
  await fs.writeFile(outputFile, output, 'utf8');

  console.log(`Generated ${path.relative(appRoot, outputFile)} with ${sortedIcons.length} icon(s).`);
}

async function collectDartFiles(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...await collectDartFiles(fullPath));
      continue;
    }

    if (entry.isFile() && entry.name.endsWith('.dart') && !entry.name.endsWith('.g.dart')) {
      files.push(fullPath);
    }
  }

  return files;
}

function extractSvgBody(svgSource) {
  const cleaned = svgSource.replace(/<!--[\s\S]*?-->/g, '').trim();
  const match = cleaned.match(/<svg[^>]*>([\s\S]*?)<\/svg>/);

  if (!match) {
    throw new Error('Unable to parse Lucide SVG document.');
  }

  return match[1];
}

function parseSvgNodes(svgBody) {
  const nodeMatches = [...svgBody.matchAll(/<(path|circle|rect|line|polyline|polygon|ellipse)\s+([^>]*?)\/>/g)];

  return nodeMatches.map((match) => {
    const tag = match[1];
    if (!supportedTags.has(tag)) {
      throw new Error(`Unsupported Lucide SVG tag "${tag}".`);
    }

    const attrs = {};
    const attrMatches = match[2].matchAll(/([a-zA-Z0-9:-]+)="([^"]*)"/g);
    for (const attr of attrMatches) {
      attrs[attr[1]] = attr[2];
    }

    return { tag, attrs };
  });
}

function renderCase(iconName, nodes) {
  const renderedNodes = nodes.map((node) => renderNode(node)).join('\n');

  return [
    `    case '${escapeDart(iconName)}':`,
    `      return [`,
    renderedNodes,
    `      ];`,
  ].join('\n');
}

function renderNode({ tag, attrs }) {
  const args = [];
  const extraAttrs = [];

  for (const [key, value] of Object.entries(attrs)) {
    if (directAttrs.has(key)) {
      args.push(`${key}: '${escapeDart(value)}'`);
    } else {
      extraAttrs.push(`'${escapeDart(key)}': '${escapeDart(value)}'`);
    }
  }

  if (extraAttrs.length > 0) {
    args.push(`attributes: {${extraAttrs.join(', ')}}`);
  }

  const prefix = args.length > 0 ? `${args.join(', ')}, ` : '';
  const tail = positionalLast.has(tag) ? '[]' : '';
  return `        ${tag}(${prefix}${tail}),`;
}

function escapeDart(value) {
  return String(value).replace(/\\/g, '\\\\').replace(/'/g, "\\'");
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exitCode = 1;
});
